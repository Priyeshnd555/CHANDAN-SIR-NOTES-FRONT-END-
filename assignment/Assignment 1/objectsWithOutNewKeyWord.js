

var tata = {

    color:'red',
    cost: 4500000,
    brand:'tata',
    Tax:'5%',
    finalCost : function (){
        return this.cost+this.Tax;
    },
};

    /* outp put of above object */
    console.log('object name :'+ tata.brand);
    console.log('cost :'+ tata.brand);
    console.log('tax :'+ tata.brand);
